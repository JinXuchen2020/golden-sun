/// 根据场景 ID 获取对应的地图数据
pub fn get_scene_map(scene_id: SceneId) -> &'static SceneMap {
    match scene_id {
        SceneId::Vale => &VALE_SCENE,
        SceneId::WildForest => &WILD_FOREST_SCENE,
        SceneId::Cave => &CAVE_SCENE,
        _ => &VALE_SCENE,
    }
}

// ── 向后兼容：Vale 村专用函数 ──

const VALE_MAP_W: i32 = constants::MAP_WIDTH as i32;
const VALE_MAP_H: i32 = constants::MAP_HEIGHT as i32;

/// 获取 Vale 村 tile（向后兼容）
pub fn get_tile(x: i32, y: i32) -> TileKind {
    VALE_SCENE.get_tile(x, y)
}

/// 检查 Vale 村 tile 是否可通行（向后兼容）
pub fn is_walkable(x: i32, y: i32) -> bool {
    VALE_SCENE.is_walkable(x, y)
}

/// 获取 Vale 村地图尺寸（向后兼容）
pub const fn map_size() -> (i32, i32) {
    (VALE_MAP_W, VALE_MAP_H)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn player_start_is_walkable() {
        assert!(is_walkable(15, 16));
    }

    #[test]
    fn map_bounds_void() {
        assert_eq!(get_tile(-1, 5), TileKind::Void);
        assert_eq!(get_tile(32, 5), TileKind::Void);
    }

    #[test]
    fn forest_is_not_walkable() {
        assert!(!is_walkable(0, 0));
    }

    #[test]
    fn bridge_is_walkable() {
        assert!(is_walkable(18, 23));
        assert!(is_walkable(19, 23));
    }

    #[test]
    fn road_is_walkable() {
        assert!(is_walkable(8, 10));
        assert!(is_walkable(15, 16));
        assert!(is_walkable(22, 10));
    }

    #[test]
    fn wild_forest_encounter_rate() {
        assert_eq!(WILD_FOREST_SCENE.encounter_rate, 6);
        assert!(WILD_FOREST_SCENE.encounter_enemies.len() > 0);
    }

    #[test]
    fn cave_map_size() {
        let (w, h) = CAVE_SCENE.size();
        assert_eq!(w, 16);
        assert_eq!(h, 16);
    }

    #[test]
    fn cave_edges_are_walls() {
        assert_eq!(CAVE_SCENE.get_tile(0, 0), TileKind::Wall);
        assert_eq!(CAVE_SCENE.get_tile(15, 15), TileKind::Wall);
    }

    #[test]
    fn cave_center_is_walkable() {
        assert!(CAVE_SCENE.is_walkable(8, 8));
        assert!(CAVE_SCENE.is_walkable(10, 10));
    }
}
