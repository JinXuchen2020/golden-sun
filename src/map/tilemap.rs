//! 地图数据 — 按场景模块化加载
//!
//! 支持多场景：Vale 村、野外森林、洞穴等。
//! 每张地图 16x16~32x32，编码对应 `TileKind::from_u8()`:
//!   0=Void 1=Grass 2=Dirt 3=Water 4=Forest 5=Wall 6=Sand 7=Snow
//!   8=Bridge 9=Stairs 10=Flower 11=Roof

use crate::engine::constants;
use crate::map::TileKind;
use crate::SceneId;

/// 场景地图数据
pub struct SceneMap {
    pub data: &'static [u8],
    pub width: i32,
    pub height: i32,
    pub encounter_rate: u32,
    pub encounter_enemies: &'static [&'static str],
}

impl SceneMap {
    #[inline]
    pub fn get_tile(&self, x: i32, y: i32) -> TileKind {
        if !(0..self.width).contains(&x) || !(0..self.height).contains(&y) {
            return TileKind::Void;
        }
        let idx = (y as usize) * (self.width as usize) + (x as usize);
        TileKind::from_u8(self.data[idx])
    }

    #[inline]
    pub fn is_walkable(&self, x: i32, y: i32) -> bool {
        self.get_tile(x, y).is_walkable()
    }

    #[inline]
    pub fn size(&self) -> (i32, i32) {
        (self.width, self.height)
    }
}

